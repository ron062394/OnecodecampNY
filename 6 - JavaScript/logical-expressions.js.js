// String Expressions or concatenation
console.log("Hello" + " " + "World!");
let greeting = "Hi";
let myName = "John";
console.log(greeting + " " + myName + ".");



// Logical expressions (&&, ||, !)
let sunny = true;
let warm = true;
console.log("Is it sunny AND warm?", sunny && warm);
console.log("Is it sunny OR warm?", sunny || warm);
console.log("NOT sunny?", !sunny);


