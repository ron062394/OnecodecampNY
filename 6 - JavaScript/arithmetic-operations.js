//1. Arithmetic Operators
let x = 21;
let y = 10;

//Addition '+'
let sum = x + y;
console.log('Sum:', sum);


//Substraction '-'
let difference = x - y;
console.log('Difference:', difference);


//Multiplication '*'
let product = x * y;
console.log('Product:', product);


//Division '/'
let quotient = x / y;
console.log("Quotient:", quotient);



//Remainder or Modulo '%'
let remainder = x % y;
console.log('Remainder', remainder);

let answer = 10 / 2 * 23 / 2;
console.log(answer)