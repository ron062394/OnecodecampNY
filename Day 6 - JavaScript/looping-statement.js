// for loop
for (let i = 1; i <= 5; i++) {
    console.log("for loop count:", i);
};


// while loop
let count = 1;
while (count <= 5) {
    console.log("while loop count:", count);
    count++;
};

// Do while loop
let counter = 10;
do {
    console.log("do while loop count:", counter);
    counter++
} while (counter <= 5);