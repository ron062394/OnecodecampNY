// Functions - allows us to write reusable block of codes that can be executed whenever needed

// Defining a function
function greet() {
    console.log('Hello, welcome to the world of functions!');
    console.log('Yehey!');
}

// Calling a function
greet();

// Defining functions with parameter
function sum(a, b) { //Parameter
    return a + b;
};


// Calling functions with parameter
console.log(sum(20, 12)); //Argument
